void	ft_putstr_non_printable(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 0 && str[i] <= 31)
		{
			str[i] = "\\";
			i++;
			str[i] = ft_to_hax(str[i]);
		}
		i++;
	}
}
